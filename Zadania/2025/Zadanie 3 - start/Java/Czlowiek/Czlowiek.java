package Czlowiek;
public class Czlowiek {
    Glowa glowa;
    Tulow tulow;
    Reka lewaReka;
    Reka prawaReka;
    Noga lewaNoga;
    Noga prawaNoga;

    Czlowiek(Glowa glowa, Tulow tulow, Reka lewaReka, Reka prawReka, Noga lewaNoga, Noga prawaNoga) {
        // Głowa
        this.glowa = glowa;

        // Tułów
        this.tulow = tulow;

        // Ręce
        this.lewaReka = lewaReka;
        this.prawaReka = prawReka;

        // Nogi
        this.lewaNoga = lewaNoga;
        this.prawaNoga = prawaNoga;
    }

}
